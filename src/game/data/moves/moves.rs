use crate::game::constants::{
    move_constants::{MoveId, NUM_ATTACKS},
    move_effect_constants,
    type_constants::Type,
};

pub struct Move {
    id: MoveId,
    effect: u8,
    power: u8,
    r#type: Type,
    accuracy: u8,
    pub pp: u8,
}

macro_rules! def_move {
    ($id:ident, $effect:ident, $power:expr, $type:ident, $accuracy_percent:expr, $pp:expr) => {{
        assert!($pp <= 40);

        Move {
            id: MoveId::$id,
            effect: move_effect_constants::$effect,
            power: $power,
            r#type: Type::$type,
            accuracy: ($accuracy_percent as u16 * 0xff / 100) as u8,
            pp: $pp,
        }
    }};
}

